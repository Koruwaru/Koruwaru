void	err_msg(char *msg, int l)
{
	ft_putstr_fd(msg, 2);
	ft_putstr_fd(" on line ", 2);
	ft_putnbr_fd(l, 2);
	ft_putchar_fd('\n', 2);
	exit(0);
}
